@Composable
fun MyApplicationTheme(
    themeMode: ThemeMode,
    darkSystemTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val lightAppTheme = when (themeMode) {
        ThemeMode.LIGHT -> true
        ThemeMode.DARK -> false
        ThemeMode.SYSTEM -> !darkSystemTheme
    }

    val appColorScheme = if (lightAppTheme) LightColors else DarkColors

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            (view.context as Activity).window.statusBarColor = appColorScheme.surface.toArgb()
            ViewCompat.getWindowInsetsController(view)?.isAppearanceLightStatusBars = lightAppTheme
        }
        // TODO check navigation colors
    }

    MaterialTheme(
        colorScheme = appColorScheme,
        typography = appTypography,
        content = content
    )
}